export async function generateMetadata({ params }: { params: { state: string, city: string } }): Promise<Metadata> {
  const cityKey = params.city.toLowerCase().replace(/-/g, '')
  const cityInfo = cityMapping[cityKey]

  if (!cityInfo) {
    return {
      title: 'City Not Found',
      description: 'The requested city was not found.'
    }
  }

  const title = `Mobile Phlebotomy ${cityInfo.name}, ${cityInfo.state} | Expert At-Home Blood Draw (2025)`
  const description = `‚≠ê Top mobile phlebotomists in ${cityInfo.name}! Licensed professionals come to you. Same-day appointments, insurance billing, HIPAA compliant. Book your at-home blood draw today.`

  return {
    title,
    description,
    keywords: `mobile phlebotomy ${cityInfo.name}, at-home blood draw ${cityInfo.name} ${cityInfo.state}, phlebotomist ${cityInfo.name}, mobile lab ${cityInfo.name}, home blood test ${cityInfo.name}`,
    openGraph: {
      title,
      description,
      type: 'website',
    },
    twitter: {
      title,
      description,
      card: 'summary_large_image',
    },
  }
}

